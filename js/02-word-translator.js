let lang = prompt("Please enter the language code \'en\' or \'fr\' or \'de\' or \'es\'");
switch(lang) {
    case 'es' : alert("Hola Mundo!");
                break;
    case 'de' : alert("Hallo Welt");
                break;
    case 'fr' : alert("Bonjour le monde");
                break;
    default   : alert("Hello World!")
}
